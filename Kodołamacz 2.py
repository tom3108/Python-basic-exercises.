class Kalk():
    def dodawanie(x , y):
        dodaj = x + y
        return dodaj
    def odejmowanie (x,y):
        odejmij = x - y
        return odejmij
    def mno (x,y):
        mno = x * y
        return mno
    def dziel (x,y):
        dziel = x/y
        return dziel

print(Kalk.dodawanie(3,4))
print(Kalk.mno(9,9))


